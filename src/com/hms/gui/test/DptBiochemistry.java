    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        pickValue();
        Biochemistry biochemistry = new Biochemistry();
        session.beginTransaction();

        for (Patient patient : patients) {
            if (patient.getRegNo().toUpperCase().equals(regNum.toUpperCase())) {
                biochemistry.setPatient(patient);
                biochemistry.setGluco(gluco);
                biochemistry.setGlucoRPP(glucoRPP);
                biochemistry.setBloodUr(bloodUr);
                biochemistry.setCreati(creati);
                biochemistry.setBun(bun);
                biochemistry.setsCholes(sCholes);
                biochemistry.setHdCholes(hdCholes);
                biochemistry.setLdlCholes(ldlCholes);
                biochemistry.setVldlCholes(vldlCholes);
                biochemistry.setTriglyce(triglyce);
                biochemistry.setsTotalLip(sTotalLip);
                biochemistry.setTestDate(testDate);

                session.save(biochemistry);
                break;
            }
        }
        session.getTransaction().commit();
    }//GEN-LAST:event_btnSaveActionPerformed

    private void txtRegNumKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtRegNumKeyReleased
        String regId = txtRegNum.getText();
        txtName.setText("No data");
        txtTestDate.setText("No Data");
        txtAge.setText("No data");
        txtGender.setText("No data");

        for (Patient patient : patients) {
            if (patient.getRegNo().toUpperCase().equals(regId.toUpperCase())) {
                txtName.setText(patient.getPatientName());
                txtTestDate.setText(patient.getRegDate().toString());
                txtAge.setText(patient.getAge());
                txtGender.setText(patient.getSex());
                txtBun.setText(patient.getBiochemistry() + "");
                break;
            }
        }
    }//GEN-LAST:event_txtRegNumKeyReleased

