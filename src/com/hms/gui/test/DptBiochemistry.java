    public void showBiochemistryReport() {
        pickValue();
        session = new MySession().getSession();
        session.beginTransaction();
        Criteria criteria = session.createCriteria(Patient.class);
        Patient patient = (Patient) criteria.add(Restrictions.eq("regNo", regNum)).uniqueResult();
        session.getTransaction().commit();

        List<Map<String, Object>> list = new ArrayList<>();
        Map<String, Object> map2 = new HashMap<String, Object>();
        list.add(map2);

        Map<String, Object> paramMap = new HashMap<String, Object>();
        paramMap.put("name", patient.getPatientName());
        paramMap.put("age", patient.getAge());
        paramMap.put("sex", patient.getSex());
        paramMap.put("doctor", patient.getReferenceBy());
        paramMap.put("date", patient.getRegDate() + "");

        paramMap.put("bun", bun + "");
        paramMap.put("gluco", gluco + "");
        paramMap.put("glucoRPP", glucoRPP + "");
        paramMap.put("bloodUr", bloodUr + "");
        paramMap.put("creati", creati + "");
        paramMap.put("sCholes", sCholes + "");
        paramMap.put("hdCholes", hdCholes + "");
        paramMap.put("ldlCholes", ldlCholes + "");
        paramMap.put("vldlCholes", vldlCholes + "");
        paramMap.put("triglyce", triglyce + "");
        paramMap.put("sTotalLip", sTotalLip + "");

        new ReportController().showReport("biochemistry", paramMap, list);
    }

