    private void setPatientDetails() {
        String regiNo = txtRegNum.getText().toUpperCase();
        if (!regiNo.equals("")) {
            for (Patient patient : patients) {
                if (patient.getRegNo().toUpperCase().equals(regiNo)) {
                    txtSex.setText(patient.getSex());
                    txtAge.setText(patient.getAge());
                    txtTestDate.setText(patient.getRegDate().toString());
                    txtName.setText(patient.getPatientName());
                    break;
                } else if (!patient.getRegNo().toUpperCase().equals(regiNo)) {
                    txtSex.setText("No Data");
                    txtAge.setText("No Data");
                    txtTestDate.setText("No Data");
                    txtName.setText("No Data");
                }
            }
        } else if (regiNo.equals("")) {
            txtSex.setText("");
            txtAge.setText("");
            txtTestDate.setText("");
            txtName.setText("");
        }
    }
