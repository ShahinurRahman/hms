    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        Patient patient = (Patient) new Query().find(Patient.class, "regNo", txtRegNum.getText());
        blood = new Blood();
        blood.setPatinetName(txtName.getText());
        blood.setBasophils(Double.parseDouble(txtBaso.getText()));
        blood.setEosinophil(Double.parseDouble(txtEosi.getText()));
        blood.setHimoglobine(Double.parseDouble(txtHimo.getText()));
        blood.setLymphocytes(Double.parseDouble(txtLimpho.getText()));
        blood.setMonocytes(Double.parseDouble(txtMono.getText()));
        blood.setNeutrophils(Double.parseDouble(txtNeutro.getText()));
        blood.setOthers(Double.parseDouble(txtOther.getText()));
        blood.setPlatelets(Double.parseDouble(txtPalete.getText()));
        blood.setTestDate(new TheDates().toDate(txtTestDate.getText()));
        blood.setPatient(patient);

        session = new MySession().getSession();
        session.beginTransaction();
        //patient.getBloodReports().add(bloodTest);
        session.save(blood);
        session.getTransaction().commit();
        session.close();

    }//GEN-LAST:event_btnSaveActionPerformed

