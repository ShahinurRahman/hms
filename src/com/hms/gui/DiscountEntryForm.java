    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        DiscountBy discountDoctor = new DiscountBy();
        discountDoctor.setContact(txtContact.getText());
        discountDoctor.setName(txtDiscountName.getText());
        discountDoctor.setDesignation(comboDiscountType.getSelectedItem().toString());
        discountDoctor.setDiscountNote(txtDiscountNote.getText());

        Session session = new MySession().getSession();
        session.beginTransaction();
        session.save(discountDoctor);
        session.getTransaction().commit();
        session.close();
    }//GEN-LAST:event_btnSaveActionPerformed

